     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->setValidator($request)->validate();
        Direccion::create($request->all());
        flash('Direccion creada satisfactoriamente')->success();
        return redirect('direcciones');
    }

    protected function setValidator(Request $request, $replaceValidationRules = [])
    {
        return Validator::make($request->all()
        , array_merge(Direccion::validationRules(), $replaceValidationRules))
        ->setAttributeNames(Direccion::attributeNames());
    }
